package com.paintology.lite.trace.drawing.Model;

public class SlideInfo {

    String slideName;
    String slideUrl;
    String slideImageUrl;
    String slideTitle;
    String slideDescription;

    public String getSlideName() {
        return slideName;
    }

    public void setSlideName(String slideName) {
        this.slideName = slideName;
    }

    public String getSlideUrl() {
        return slideUrl;
    }

    public void setSlideUrl(String slideUrl) {
        this.slideUrl = slideUrl;
    }

    public String getSlideImageUrl() {
        return slideImageUrl;
    }

    public void setSlideImageUrl(String slideImageUrl) {
        this.slideImageUrl = slideImageUrl;
    }

    public String getSlideTitle() {
        return slideTitle;
    }

    public void setSlideTitle(String slideTitle) {
        this.slideTitle = slideTitle;
    }

    public String getSlideDescription() {
        return slideDescription;
    }

    public void setSlideDescription(String slideDescription) {
        this.slideDescription = slideDescription;
    }
}
