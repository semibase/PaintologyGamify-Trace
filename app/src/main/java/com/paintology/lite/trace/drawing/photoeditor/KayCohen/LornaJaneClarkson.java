package com.paintology.lite.trace.drawing.photoeditor.KayCohen;


public class LornaJaneClarkson {

    String DirName, FrameName, MaskName;

    public LornaJaneClarkson(String dirName, String frameName, String maskName) {
        DirName = dirName;
        FrameName = frameName;
        MaskName = maskName;
    }

    public String getDirName() {
        return DirName;
    }

    public void setDirName(String dirName) {
        DirName = dirName;
    }

    public String getFrameName() {
        return FrameName;
    }

    public void setFrameName(String frameName) {
        FrameName = frameName;
    }

    public String getMaskName() {
        return MaskName;
    }

    public void setMaskName(String maskName) {
        MaskName = maskName;
    }
}
